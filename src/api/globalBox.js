import http from './http'
import QS from 'qs'

function strSplicing(params) {
  let url = ''
  if (params) {
    Object.keys(params).forEach(key => {
      url += `${key}=${params[key]}&`
    })
    url = url.slice(0, -1)
  }
  return url
}

export const gb = {
  basicData(params) {
    return http.post('/customDialogr/get', QS.stringify(params))
  }
}
